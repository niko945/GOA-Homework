def multiply(a, b):
   return a * b

def even_or_odd(number):
    if number % 2 == 0:
        return "even"
    else:
        return "odd"
    
def number_to_string(num):
    return str(num)

def solution(string):
    return string[::-1]

def make_negative(number):
    return -abs(number)

def opposite(number):
    return -number